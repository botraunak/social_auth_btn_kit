/// The [outlined] variant displays a button with a outline with brand color.
enum AppleTypeVariants {
  normal,
  outlined,
}

/// AppleThemeVariants
/// This enum determines the theme of the Apple button.
/// The [white] variant displays a button with a white theme.
/// The [black] variant displays a button with a black theme.
/// The [white] variant is the default variant.
enum AppleThemeVariants {
  black,
  white,
}
