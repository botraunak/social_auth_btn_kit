/// FacebookTypeVariants
/// This enum determines the type of Facebook button to display.
/// The [normal] variant displays a button with a Facebook logo and text.
/// The [outlined] variant displays a button with a outline with brand color.
/// The [normal] variant is the default variant.
enum FacebookTypeVariants {
  normal,
  outlined,
}

/// FacebookThemeVariants
/// This enum determines the theme of the Facebook button.
/// The [light] variant displays a button with a light theme.
/// The [dark] variant displays a button with a dark theme.
/// The [light] variant is the default variant.
enum FacebookThemeVariants {
  light,
  dark,
}

/// GoogleTypeVariants
/// This enum determines the type of Google button to display.
/// The [light] variant displays a button with a Google logo and text.
/// The [dark] variant displays a button in dark theme.
/// The [neutral] variant displays a grey button
enum GoogleThemeVariants {
  light,
  dark,
  neutral,
}

